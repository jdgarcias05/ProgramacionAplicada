import numpy as np

arreglo = np.arange(4)

2 + arreglo         # array([2, 3, 4, 5])
arreglo - 10        # array([-10, -9, -8, -7])
arreglo - arreglo   # array([0, 0, 0, 0])
6 * arreglo         # array([0, 6, 12, 18])
arreglo * arreglo   # array([0, 1, 4, 9])
arreglo / 2         # array([0. , 0.5, 1. , 1.5])
